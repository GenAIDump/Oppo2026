@app.post("/relationships/link-post-to-candidate", status_code=status.HTTP_201_CREATED)
async def link_post_to_candidate(
    post_id: str,
    candidate_id: str,
    db: Neo4jClient = Depends(get_db)
):
    """Creates a POSTED_BY relationship between a SocialPost and a Candidate."""
    logger.info(f"MCP: Linking post '{post_id}' to candidate '{candidate_id}'")
    cypher = """
    MATCH (p:SocialPost {id: $post_id})
    MATCH (c:Candidate {id: $candidate_id})
    MERGE (p)-[r:POSTED_BY]->(c)
    RETURN type(r) as relationship_type
    """
    params = {'post_id': post_id, 'candidate_id': candidate_id}
    try:
        result = db.execute_query(cypher, params, write=True)
        if result and result[0].get('relationship_type') == 'POSTED_BY':
            logger.info(f"MCP: Successfully linked post '{post_id}' to candidate '{candidate_id}'.")
            return {"message": "Relationship created successfully."}
        else:
            logger.warning(f"MCP: Failed to verify relationship creation between post '{post_id}' and candidate '{candidate_id}'. Nodes might be missing.")
            # Check if nodes exist before attempting merge?
            raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Post or Candidate node not found, or relationship merge failed.")
    except Neo4jError as e:
        logger.error(f"MCP: Neo4jError linking post to candidate: {e}")
        raise HTTPException(status_code=status.HTTP_500_INTERNAL_SERVER_ERROR, detail=f"Database error: {e}")
