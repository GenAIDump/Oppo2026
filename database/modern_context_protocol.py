@app.post("/context/candidate", response_model=MCPGetCandidateContextResponse)
async def get_candidate_context(
    request: MCPGetCandidateContextRequest,
    db: Neo4jClient = Depends(get_db)
):
    """Retrieves recent SocialPost context for a given candidate."""
    logger.info(f"MCP: Getting context for candidate '{request.candidate_id}', limit={request.limit}")

    params = {
        'candidate_id': request.candidate_id,
        'limit': request.limit
    }
    # Base query to match candidate and related posts
    cypher = """
    MATCH (c:Candidate {id: $candidate_id})<-[:POSTED_BY]-(p:SocialPost)
    """
    # Optional filtering by date
    if request.before_date:
        params['before_date'] = request.before_date
        cypher += "WHERE p.published_at < $before_date "

    # Ordering and limiting
    cypher += """
    RETURN p
    ORDER BY p.published_at DESC
    LIMIT $limit
    """

    try:
        results = db.execute_query(cypher, params, write=False)
        posts = []
        if results:
            for record in results:
                post_data = record['p']
                 # Convert to Pydantic model, handling potential datetime strings
                if isinstance(post_data.get('published_at'), str):
                    post_data['published_at'] = datetime.fromisoformat(post_data['published_at'].replace('Z', '+00:00'))
                if isinstance(post_data.get('ingested_at'), str):
                    post_data['ingested_at'] = datetime.fromisoformat(post_data['ingested_at'].replace('Z', '+00:00'))
                if isinstance(post_data.get('analysis_completed_at'), str):
                    post_data['analysis_completed_at'] = datetime.fromisoformat(post_data['analysis_completed_at'].replace('Z', '+00:00'))

                posts.append(SocialPost(**post_data))
        logger.info(f"MCP: Retrieved {len(posts)} context posts for candidate '{request.candidate_id}'.")
        return MCPGetCandidateContextResponse(success=True, posts=posts)
    except Neo4jError as e:
        logger.error(f"MCP: Neo4jError getting candidate context: {e}")
        return MCPGetCandidateContextResponse(success=False, error=f"Neo4jError: {e}")
    except Exception as e:
        logger.error(f"MCP: Unexpected error getting candidate context: {e}")
        return MCPGetCandidateContextResponse(success=False, error=f"Unexpected error: {e}")


