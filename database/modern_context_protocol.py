@app.post("/nodes/store", response_model=MCPStoreNodeResponse)
async def store_node(
    request: MCPStoreNodeRequest,
    db: Neo4jClient = Depends(get_db)
):
    """
    Stores (Creates or Merges) a single node (e.g., Candidate, SocialPost) in Neo4j.
    Uses MERGE if unique_field is provided, otherwise CREATE.
    """
    logger.info(f"MCP: Storing node with label '{request.node_label}'")
    properties = request.properties.copy() # Avoid modifying the original request

    # Ensure standard fields are present and correctly typed
    properties['updated_at'] = datetime.utcnow()
    if 'created_at' not in properties:
        properties['created_at'] = datetime.utcnow()
    if 'id' not in properties:
         # If using UUIDs generated by the model, they should be in properties already.
         # If generating here, ensure it's consistent.
         # properties['id'] = str(uuid.uuid4()) # Or retrieve from model default
         pass # Assume ID is handled by the pydantic model or passed in properties


    # Prepare parameters for Cypher, ensuring nested dicts/lists are handled if necessary
    # The Neo4jClient's _serialize_value handles datetime etc.
    params = {'props': properties}

    cypher = ""
    node_var = "n" # Variable name in Cypher

    if request.unique_field and request.unique_field in properties:
        # Use MERGE based on the unique field
        unique_value = properties[request.unique_field]
        params['unique_value'] = unique_value
        cypher = f"""
        MERGE ({node_var}:{request.node_label} {{ {request.unique_field}: $unique_value }})
        ON CREATE SET {node_var} = $props
        ON MATCH SET {node_var} += $props, {node_var}.updated_at = datetime()
        RETURN {node_var}.id as node_id
        """
        logger.debug(f"MCP Store: Using MERGE on {request.node_label}.{request.unique_field}")
    elif 'id' in properties:
         # Use MERGE based on the internal ID (preferred way to update/create)
        params['node_id'] = properties['id']
        cypher = f"""
        MERGE ({node_var}:{request.node_label} {{ id: $node_id }})
        ON CREATE SET {node_var} = $props
        ON MATCH SET {node_var} += $props, {node_var}.updated_at = datetime()
        RETURN {node_var}.id as node_id
        """
        logger.debug(f"MCP Store: Using MERGE on {request.node_label}.id")
    else:
        # Use CREATE if no unique field or ID is specified (less common for updates)
        cypher = f"""
        CREATE ({node_var}:{request.node_label})
        SET {node_var} = $props
        RETURN {node_var}.id as node_id
        """
        logger.debug(f"MCP Store: Using CREATE for {request.node_label}")


    try:
        result = db.execute_query(cypher, parameters=params, write=True)
        if result and 'node_id' in result[0]:
            node_id = result[0]['node_id']
            logger.info(f"MCP: Node '{node_id}' stored successfully.")
            return MCPStoreNodeResponse(success=True, node_id=node_id, message="Node stored successfully.")
        else:
             logger.error(f"MCP: Node storage query did not return node_id. Cypher: {cypher}")
             return MCPStoreNodeResponse(success=False, error="Node stored, but failed to retrieve ID.")
    except Neo4jError as e:
        logger.error(f"MCP: Neo4jError storing node: {e}")
        return MCPStoreNodeResponse(success=False, error=f"Neo4jError: {e}")
    except Exception as e:
        logger.error(f"MCP: Unexpected error storing node: {e}")
        return MCPStoreNodeResponse(success=False, error=f"Unexpected error: {e}")


@app.put("/nodes/update", response_model=MCPUpdateNodeResponse)
async def update_node(
    request: MCPUpdateNodeRequest,
    db: Neo4jClient = Depends(get_db)
):
    """Updates properties of an existing node identified by its internal ID."""
    logger.info(f"MCP: Updating node '{request.node_id}'")
    properties_to_update = request.properties.copy()
    properties_to_update['updated_at'] = datetime.utcnow() # Always update timestamp

    # Prepare parameters
    params = {'node_id': request.node_id, 'props': properties_to_update}

    # Build SET clauses dynamically to avoid overwriting the entire node
    set_clauses = [f"n.{key} = $props.{key}" for key in properties_to_update]
    if not set_clauses:
        return MCPUpdateNodeResponse(success=False, node_id=request.node_id, error="No properties provided for update.")

    cypher = f"""
    MATCH (n {{ id: $node_id }})
    SET {', '.join(set_clauses)}
    RETURN n.id as node_id
    """

    try:
        result = db.execute_query(cypher, parameters=params, write=True)
        if result and 'node_id' in result[0]:
            node_id = result[0]['node_id']
            logger.info(f"MCP: Node '{node_id}' updated successfully.")
            return MCPUpdateNodeResponse(success=True, node_id=node_id, message="Node updated.")
        else:
            # This could mean the node with the given ID wasn't found
            logger.warning(f"MCP: Node update query did not return ID or node '{request.node_id}' not found.")
            return MCPUpdateNodeResponse(success=False, node_id=request.node_id, error="Node not found or update failed.")
    except Neo4jError as e:
        logger.error(f"MCP: Neo4jError updating node: {e}")
        return MCPUpdateNodeResponse(success=False, node_id=request.node_id, error=f"Neo4jError: {e}")
    except Exception as e:
        logger.error(f"MCP: Unexpected error updating node: {e}")
        return MCPUpdateNodeResponse(success=False, node_id=request.node_id, error=f"Unexpected error: {e}")


